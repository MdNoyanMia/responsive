$("#hide").click(function(){
    $(".box").hide(1000)
})

$("#show").click(function(){
    $(".box").show(1000)
})

$("#both").click(function(){
    $(".box").toggle(1000)
})

$("#slideup").click(function(){
    $(".box").slideUp(1000)
})

$("#slidedown").click(function(){
    $(".box").slideDown(1000)
})
$("#slideUpDown").click(function(){
    $(".box").slideToggle(1000)
})

